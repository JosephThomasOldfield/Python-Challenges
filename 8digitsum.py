'''
If I add up the digits of the number 19082 I would get
1+9+0+8+2=20. Add the digits of the number:
371072875339021027987979982208375902465101357402504637693767749000971264812489697007
805041701826053874324986199524741059
'''
number1 = 371072875339021027987979982208375902465101357402504637693767749000971264812489697007
number2 = 805041701826053874324986199524741059


def sum_digits(numbervalue):
    s = 0
    while numbervalue:
        s += numbervalue % 10
        numbervalue //= 10
    print(s)

sum_digits(number1)
sum_digits(number2)

